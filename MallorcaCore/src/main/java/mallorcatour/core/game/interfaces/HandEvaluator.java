package mallorcatour.core.game.interfaces;

/**
 * Простой интерфейс, который определяет значимость комбинации по картам.
 * Смотреть его реализации для того, чтобы было понятнее.
 */
public interface HandEvaluator {
    /**
     * Для набора карт с лучшей комбинацией всегда возвращает
     * число больше, чем для набора карт с комбинацией хуже.
     */
    int evaluate(int[] allCards);
}
